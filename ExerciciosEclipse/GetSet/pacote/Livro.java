package pacote;

public class Livro {
	private String titulo;
	private String autor;
	private int anoPublicação;
	
	
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String Stitulo) {
		this.titulo = Stitulo;
	}
	public String getAutor() {
		return autor;
	}
	public void setAutor(String Sautor) {
		this.autor = Sautor;
	}
	public int getAnoPublicação() {
		return anoPublicação;
	}
	public void setAnoPublicação(int SanoPublicação) {
		this.anoPublicação = SanoPublicação;
	}
	
	
}
